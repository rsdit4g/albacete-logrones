#!/usr/bin/env node
// Merge all squad_parts/*.json files into src/data/squads.js
// Run: node scripts/merge_squads.js

import { readFileSync, writeFileSync, readdirSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const partsDir = join(__dirname, "../src/data/squad_parts");
const outFile = join(__dirname, "../src/data/squads.js");

// Canonical club name normalization — maps agent variants to real-seasons.js names
